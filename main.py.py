print(f"\nEducation values match: {sales_edu == prospect_edu}")
print(f"Sales Education: {sorted(sales_edu)}")
print(f"Prospect Education: {sorted(prospect_edu)}")

# ===== DEFINE COMMON FEATURES =====
common_features = [
    'Age', 'MaritalStatus', 'Gender', 'YearlyIncome',
    'TotalChildren', 'NumberChildrenAtHome', 'Education',
    'Occupation', 'HouseOwnerFlag', 'NumberCarsOwned',
    'StateProvinceCode', 'PostalCode'
]

# Extract feature matrices
X_train_raw = df_sales[common_features].copy()
y_all = df_sales['Buyer'].copy()
X_prospect_raw = df_prospects[common_features].copy()

print(f"\nTraining features shape: {X_train_raw.shape}")
print(f"Target variable shape: {y_all.shape}")
print(f"Prospect features shape: {X_prospect_raw.shape}")

# ===== STEP 3: FEATURE ENGINEERING =====
print("\n" + "=" * 60)
print("STEP 3: FEATURE ENGINEERING & ENCODING")
print("=" * 60)

# ===== BINARY ENCODING =====
# MaritalStatus: M=1 (Married), S=0 (Single)
# Gender: M=1 (Male), F=0 (Female)
binary_mappings = {
    'MaritalStatus': {'M': 1, 'S': 0},
    'Gender': {'M': 1, 'F': 0}
}

for col, mapping in binary_mappings.items():
    X_train_raw[col] = X_train_raw[col].map(mapping)
    X_prospect_raw[col] = X_prospect_raw[col].map(mapping)

print("Binary encoding applied: MaritalStatus, Gender")

# ===== ONE-HOT ENCODING =====
# Must encode BOTH datasets together to ensure identical feature columns
# This prevents feature mismatch between training and scoring

multi_cat_cols = ['Education', 'Occupation', 'StateProvinceCode', 'PostalCode']

# Add source labels before combining
X_train_raw['_source'] = 'train'
X_prospect_raw['_source'] = 'prospect'

# Combine datasets for consistent encoding
combined = pd.concat([X_train_raw, X_prospect_raw], axis=0, ignore_index=True)

# Apply one-hot encoding
combined_encoded = pd.get_dummies(
    combined,
    columns=multi_cat_cols,
    drop_first=True
)

# Split back into training and prospect datasets
X_all_encoded = combined_encoded[combined_encoded['_source'] == 'train'].drop(
    columns=['_source']
).reset_index(drop=True)

X_prospects_encoded = combined_encoded[combined_encoded['_source'] == 'prospect'].drop(
    columns=['_source']
).reset_index(drop=True)

print(f"After encoding - Training features: {X_all_encoded.shape}")
print(f"After encoding - Prospect features: {X_prospects_encoded.shape}")
print(f"Feature columns (first 10): {list(X_all_encoded.columns[:10])}")

# ===== TRAIN-TEST SPLIT =====
X_train, X_test, y_train, y_test = train_test_split(
    X_all_encoded, y_all,
    test_size=0.2,
    random_state=42,
    stratify=y_all  # Preserve class balance in both splits
)

