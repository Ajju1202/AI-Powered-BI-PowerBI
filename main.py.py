# ===== FIX TRUNCATED EDUCATION VALUES =====
# ProspectiveBuyer.csv has truncated Education values
# e.g., "Partial Hi" should be "Partial High School"
print("\nProspect Education values (before fix):")
print(df_prospects['Education'].unique())

education_mapping = {
    'Partial Hi': 'Partial High School',
    'Partial High School': 'Partial High School',
    'High Schoo': 'High School',
    'High School': 'High School',
    'Graduate D': 'Graduate Degree',
    'Graduate Degree': 'Graduate Degree',
    'Partial Co': 'Partial College',
    'Partial College': 'Partial College',
    'Bachelors': 'Bachelors'
}

df_prospects['Education'] = df_prospects['Education'].apply(
    lambda x: education_mapping.get(x, x)
)

print("\nProspect Education values (after fix):")
print(df_prospects['Education'].unique())

# Verify that Education values now match between datasets
sales_edu = set(df_sales['Education'].unique())
